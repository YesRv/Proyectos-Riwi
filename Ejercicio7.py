##Promedio de tres números

num1 = float(input("Por favor ingresa un número entero  "))
num2 = float(input("Ahora ingresa otro número entero  "))
num3 = float(input("Ahora ingresa otro número entero  "))
promedio = (num1 + num2 + num3)/3

print("El promedio de los 3 numeros ingresados es:  ", promedio)